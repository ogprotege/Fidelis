/** Day-of-year (1-366) in local time. */
function dayOfYear(d: Date): number {
  const start = new Date(d.getFullYear(), 0, 0);
  return Math.floor((d.getTime() - start.getTime()) / 86_400_000);
}

export function verseOfTheDay(date: Date = new Date()): VotdRef {
  const idx = (dayOfYear(date) + date.getFullYear()) % VOTD_CYCLE.length;
  return VOTD_CYCLE[idx];
}

export function formatVotdRef(ref: VotdRef, bookName: string): string {
  const range = ref.endVerse ? `${ref.verse}–${ref.endVerse}` : `${ref.verse}`;
  return `${bookName} ${ref.chapter}:${range}`;
}
